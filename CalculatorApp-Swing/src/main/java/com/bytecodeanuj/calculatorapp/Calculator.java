    private void equalBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalBtnActionPerformed
        if(tf.getText()!=null){
            isEqualPressed = true;
            val2=stringToDouble(tf.getText());
            result = calc(val1, val2, operator);
            tf.setText(result+"");
        }
    }//GEN-LAST:event_equalBtnActionPerformed

    double calc(Double value1, Double value2,char operator){
        switch(operator) {

            case '+':
                result = val1 + val2; break;
            case '-':
                result = val1 - val2; break;
            case '/':
                if (val2 != 0)
                    result = val1 / val2; break;
            case '*':
                result = val1 * val2; break;

            default: JOptionPane.showMessageDialog(rootPane, "Error in expression!");
        }
       
        return result;
      
       
    }
    
    
